// Helper: get lesson by id
CURRICULUM.getLesson = id => {
  for (const lvl of CURRICULUM.levels) {
    const found = CURRICULUM[lvl].lessons.find(l => l.id === id);
    if (found) return found;
  }
  return null;
};

// Helper: get all lesson ids in order
CURRICULUM.allIds = () =>
  CURRICULUM.levels.flatMap(lvl => CURRICULUM[lvl].lessons.map(l => l.id));

// Unlock rule: a level is unlocked if prev level's last lesson is done
CURRICULUM.isLevelUnlocked = (level, completedLessons) => {
  const idx = CURRICULUM.levels.indexOf(level);
  if (idx === 0) return true;
  const prevLevel = CURRICULUM.levels[idx - 1];
  const prevLessons = CURRICULUM[prevLevel].lessons;
  const lastId = prevLessons[prevLessons.length - 1].id;
  return completedLessons.includes(lastId);
};
